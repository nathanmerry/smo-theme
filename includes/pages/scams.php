	<p><strong>At Season Marketing Limited, we value our customers and we will always prioritise your safety and security. We share the following information with the aim of helping you to protect your personal information and to hopefully prevent you from becoming a victim of fraud in the future. If you are concerned that scammers may have targeted you already, this page will also guide you to report it promptly.</strong></p>
<h2>Lending rules to remember</h2>
<ul>
<li>An FCA (Financial Conduct Authority) registered firm will never ask you to pay a fee in advance for a loan</li>
<li>Never share personal information with companies or individuals without first properly verifying their identity</li>
<li>Never send payments to an individual or company that you are not completely certain you have a valid loan agreement with</li>
<li>Beware of companies who contact you about a loan you don’t recall applying for</li>
</ul>
<h2>Scams by telephone, email, letter or SMS</h2>
<p>It’s not always easy to spot a scam. Fraudsters are well practised at appearing helpful and genuine and can often apply pressure to push you to act quickly. When a fraudster contacts you by telephone, email, letter or SMS, they will often use the guise of being an employee at a genuine loans company in order to win your trust.  In a common scam, they will tell you that you have qualified for a loan and need to pay a fee in order for the funds to be released to you. Unfortunately, once the fee is paid, the loan does not materialise. Worse still, the fraudsters may use your personal details to try and gain access to your accounts or attempt to commit further fraud in your name.  </p>
<h2>Recognising advance fee and clone firm fraud</h2>
<p>A genuine FCA registered company will never ask you to pay an advance fee for a loan. In an attempt to appear genuine, some fraudsters operate as what are known as Clone Firms. They use the names of legitimate companies along with forged or copied lender licenses, faked loan agreements and confirmation letters to make their loan offer to you seem real.</p>
<p>At Season Marketing Limited we only ever work with legitimate loan companies. None of the lenders we work with will ever ask you for an upfront payment. This includes requests for payment by prepaid debit cards or money transfers in order to release loan funds.</p>
<h2>How to report suspected advance fee and clone firm fraud</h2>
<p>If you think an unauthorised lender or a clone firm may have targeted you and they have attempted to obtain or been successful in extracting personal details or a fee from you, it’s important to report it straight away. You can contact the Financial Conduct Authority online or via their helpline on 0800 111 6768. The National Fraud and Cybercrime reporting centre, Action Fraud, can be contacted through the Action Fraud website or by calling 0300 123 2040. If you have concerns or queries we can help with, please email us at <a href="mailto:compliance@seasonfinance.com">compliance@seasonfinance.com</a>.</p>

