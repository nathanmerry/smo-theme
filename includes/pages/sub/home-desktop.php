						<p><strong>Collections Process</strong></p>

						<p>Once you have received your loan amount, if you are unsure that you will be able to meet any arranged repayments, or if you think there will be any difficulty repaying any part of your loan, then you must contact the provider of your loan directly as soon as you become aware of this. Contacting your lender will provide you with the best opportunity to perhaps reschedule payments or discuss further options in more detail to avoid non-payment entirely. Loan Paramount wishes to make customers aware however, that changing the details or repayments of your loan amount could incur further charges or interest, the details of which can only be discussed directly with the lender of your payday loan. If no attempt is made to contact your lender and update them on any circumstantial changes or make the lender aware of any difficulty in paying back what you owe; they reserve the right to pass on your details to third party debt collections agencies or organisations that will attempt to recover the debt.</p>
						<p><strong>Implications of Non-Payment</strong></p>

						<p>The implications of non-payment of your loan could result in the details being included within your credit report, hindering future attempts at securing credit or finance, in addition to further charges potentially being added by the lender. Where you miss a payment entirely, you will often incur a charge/fee that will be added on to the total amount that you owe. Upon a missed payment, lenders will often try to contact you by telephone or letter in order to recover the funds and they may be able to arrange a new re-payment date with you. If you are struggling, or think there is a possibility that you will not be able to meet repayments, Loan Paramount strongly recommends that you contact the lender in the first instance.</p>
						<p><strong>Late Payment</strong></p>
						<p>If you become aware that you will be late meeting an arrange repayment of your loan, you need to directly contact your loan provider to discuss this. There may be options available to rearrange the date of repayment however this usually incurs a charge on top of the arranged repayment. Some lenders may also charge you for a late repayment fee which will vary on the lender, but could range between £1 - £15. If you require further information regarding late payments you should please refer to the terms and conditions provided with your loan agreement or contact your lender to discuss this in detail.</p>
						<p><strong>Renewal Policy</strong></p>

						<p>If you find the need to renew or extend your loan, you will need to directly contact your lender as soon as possible. Loan Paramount does not recommend or endorse the renewal of your payday loan, or the rollover of your loan into a new period. In most cases, you will be charged the same amount of interest originally charged on top of your loan. In the event of renewal this interest will be based on the total amount you now owe, and now just the original amount borrowed. If you have failed to meet your payments as per the terms and conditions of your loan agreement, a loan renewal or extension may be automatic – meaning that charges could be added to your account.</p>									
						
